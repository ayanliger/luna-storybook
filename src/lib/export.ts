import { StoryPage } from "./types";
import { jsPDF } from "jspdf";
import JSZip from "jszip";

export async function exportMarkdown(
  title: string,
  pages: StoryPage[]
): Promise<void> {
  const slug = slugify(title);
  const zip = new JSZip();
  const imgFolder = zip.folder("images")!;

  // -- Markdown (for VS Code / GitHub) --
  let md = `<div align="center">\n\n# ${title}\n\n---\n\n</div>\n\n`;

  // -- HTML (for browsers) --
  let htmlPages = "";

  for (const page of pages) {
    let imgName = "";
    if (page.image.data) {
      const ext = page.image.mimeType.split("/")[1] || "png";
      imgName = `page-${page.pageNumber}.${ext}`;
      imgFolder.file(imgName, page.image.data, { base64: true });

      md += `<div align="center">\n\n`;
      md += `![Page ${page.pageNumber}](images/${imgName})\n\n`;
      md += `</div>\n\n`;
    }

    md += `<div style="text-align:justify">\n\n${page.poem}\n\n</div>\n\n`;
    md += `<div align="center">\n\n${page.pageNumber}\n\n---\n\n</div>\n\n`;

    // HTML page section
    const escapedPoem = page.poem
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;");
    htmlPages += `
      <section class="story-page">
        ${imgName ? `<div class="painting"><img src="images/${imgName}" alt="Page ${page.pageNumber}" /></div>` : ""}
        <div class="prose">${escapedPoem}</div>
        <div class="page-num">${page.pageNumber}</div>
        <div class="divider"></div>
      </section>`;
  }

  md += `<div align="center">\n\n*Generated by Luna*\n\n</div>\n`;

  const html = buildHTML(title, htmlPages);

  zip.file(`${slug}.md`, md);
  zip.file(`${slug}.html`, html);
  const blob = await zip.generateAsync({ type: "blob" });
  downloadBlob(blob, `${slug}.zip`);
}

