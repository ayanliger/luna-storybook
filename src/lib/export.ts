export async function exportPDF(
  title: string,
  pages: StoryPage[]
): Promise<void> {
  const pdf = new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" });
  const pageWidth = pdf.internal.pageSize.getWidth();
  const pageHeight = pdf.internal.pageSize.getHeight();
  const margin = 25;
  const contentWidth = pageWidth - margin * 2;
  const cx = pageWidth / 2;

  // -- Title page --
  // Centered title — Times (closest built-in to Cormorant Garamond)
  pdf.setFont("times", "normal");
  pdf.setFontSize(36);
  pdf.setTextColor(26, 26, 31);
  const titleLines = pdf.splitTextToSize(title, contentWidth);
  const titleBlockHeight = titleLines.length * 14;
  const titleY = pageHeight * 0.4 - titleBlockHeight / 2;
  pdf.text(titleLines, cx, titleY, { align: "center", lineHeightFactor: 1.3 });

  // Thin divider
  const dividerY = titleY + titleBlockHeight + 6;
  pdf.setDrawColor(138, 138, 148);
  pdf.setLineWidth(0.25);
  pdf.line(cx - 16, dividerY, cx + 16, dividerY);

  // Subtitle — sans-serif for contrast
  pdf.setFont("helvetica", "normal");
  pdf.setFontSize(9);
  pdf.setTextColor(138, 138, 148);
  pdf.text("Generated by Luna", cx, dividerY + 8, { align: "center" });

  // -- Story pages --
  for (const page of pages) {
    pdf.addPage();
    pdf.setTextColor(26, 26, 31);

    // Painting — centered at ~75% content width to match on-screen proportions
    const imgWidth = contentWidth * 0.75;
    const imgX = (pageWidth - imgWidth) / 2;
    let y = margin + 4;

    if (page.image.data) {
      const imgData = `data:${page.image.mimeType};base64,${page.image.data}`;
      const imgHeight = imgWidth * 0.65;

      // Subtle shadow (a light gray rectangle offset behind the image)
      pdf.setFillColor(230, 230, 234);
      pdf.rect(imgX + 1.5, y + 1.5, imgWidth, imgHeight, "F");

      pdf.addImage(imgData, "JPEG", imgX, y, imgWidth, imgHeight);
      y += imgHeight + 14;
    }

    // Prose — centered narrow column, justified serif text
    const proseWidth = contentWidth * 0.65;
    const proseX = (pageWidth - proseWidth) / 2;
    pdf.setFont("times", "normal");
    pdf.setFontSize(11);
    const textLines: string[] = pdf.splitTextToSize(page.poem, proseWidth);
    const lineHeight = 5.5;
    for (let i = 0; i < textLines.length; i++) {
      const line = textLines[i].trim();
      const isLastLine = i === textLines.length - 1;
      // Justify all lines except the last one in the paragraph
      if (!isLastLine && line.length > 0) {
        const words = line.split(/\s+/);
        if (words.length > 1) {
          const totalWordWidth = words.reduce(
            (sum, w) => sum + pdf.getTextWidth(w),
            0
          );
          const extraSpace =
            (proseWidth - totalWordWidth) / (words.length - 1);
          let cursorX = proseX;
          for (const word of words) {
            pdf.text(word, cursorX, y);
            cursorX += pdf.getTextWidth(word) + extraSpace;
          }
        } else {
          pdf.text(line, proseX, y);
        }
      } else {
        pdf.text(line, proseX, y);
      }
      y += lineHeight;
    }

    // Page number — centered near bottom, sans-serif
    pdf.setFont("helvetica", "normal");
    pdf.setFontSize(8);
    pdf.setTextColor(138, 138, 148);
    pdf.text(String(page.pageNumber), cx, pageHeight - margin, {
      align: "center",
    });

    // Bottom divider line
    pdf.setDrawColor(200, 200, 206);
    pdf.setLineWidth(0.2);
    pdf.line(cx - 10, pageHeight - margin + 3, cx + 10, pageHeight - margin + 3);
  }

  pdf.save(`${slugify(title)}.pdf`);
}

function slugify(text: string): string {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function downloadBlob(blob: Blob, filename: string): void {
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
